'''
Проверка корректности даты
Вы работаете над разработкой программы для проверки корректности даты,
введенной пользователем. На вход будет подаваться дата в формате "день.месяц.
год". Ваша задача - создать программу, которая проверяет, является ли введенная
 дата корректной или нет.
Ваша программа должна предоставить ответ "True" (дата корректна) или "False"
(дата некорректна) в зависимости от результата проверки.
Пример использования На входе:
date_to_prove = 15.4.2023
На выходе:
True
На входе:
date_to_prove = 31.6.2022
На выходе:
False
'''


def is_valid_date(date):
    day, month, year = map(int, date.split('.'))

    # Проверка корректности дня
    if month in (1, 3, 5, 7, 8, 10, 12):
        max_day = 31
    elif month in (4, 6, 9, 11):
        max_day = 30
    else:  # Февраль
        if year % 4 == 0 and (year % 100 != 0 or year % 400 == 0):
            max_day = 29
        else:
            max_day = 28

    if day < 1 or day > max_day:
        return False  # День некорректный

    # Проверка корректности месяца
    if month < 1 or month > 12:
        return False  # Месяц некорректный

    # Проверка корректности года
    if year < 1:
        return False  # Год отрицательный

    return True  # Все проверки пройдены


# Пример использования
date_to_prove = "15.4.2023"
print(is_valid_date(date_to_prove))  # Выведет True
